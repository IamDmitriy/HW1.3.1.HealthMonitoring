package com.example.healthmonitoring;

public class User {
    private String surname;
    private String name;
    private String patronymic;
    private int age;

    public User(String surname, String name, String patronymic, int age) {
        this.surname = surname;
        this.name = name;
        this.patronymic = patronymic;
        this.age = age;
    }

}
